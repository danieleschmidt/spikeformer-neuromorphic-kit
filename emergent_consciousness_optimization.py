    def _generate_cognitive_tasks(self, n_samples: int, input_dim: int, output_dim: int) -> List[Tuple[np.ndarray, np.ndarray]]:
        """Generate synthetic cognitive reasoning tasks"""
        tasks = []
        
        for i in range(n_samples):
            # Create input patterns that require integration
            x = np.random.randn(input_dim)
            
            # Target requires integration across multiple dimensions
            y = np.zeros(output_dim)
            
            # Complex reasoning: output depends on pattern integration
            for j in range(output_dim):
                # Different integration patterns for each output
                integration_indices = np.random.choice(input_dim, size=min(5, input_dim), replace=False)
                y[j] = np.tanh(np.sum(x[integration_indices]) / len(integration_indices))
            
            tasks.append((x, y))
        
        return tasks
    
    def _generate_few_shot_cognitive_data(self, n_classes: int, examples_per_class: int, 
                                        test_samples: int, input_dim: int, output_dim: int):
        """Generate few-shot learning cognitive tasks"""
        few_shot_scenarios = []
        
        for scenario in range(3):  # Multiple scenarios
            class_examples = []
            
            # Generate examples for each class
            for class_id in range(n_classes):
                class_pattern = np.random.randn(input_dim) * 0.5 + class_id  # Class-specific pattern
                examples = []
                
                for ex in range(examples_per_class):
                    x = class_pattern + np.random.randn(input_dim) * 0.2  # Small variations
                    
                    # Class-specific target
                    y = np.zeros(output_dim)
                    y[class_id % output_dim] = 1.0  # One-hot-like encoding
                    
                    examples.append((x, y))
                
                class_examples.append(examples)
            
            # Generate test cases
            test_cases = []
            for test_idx in range(test_samples):
                test_class = np.random.randint(n_classes)
                class_pattern = np.random.randn(input_dim) * 0.5 + test_class
                test_x = class_pattern + np.random.randn(input_dim) * 0.3
                
                test_y = np.zeros(output_dim)
                test_y[test_class % output_dim] = 1.0
                
                test_cases.append((test_x, test_y))
            
            # Flatten class examples for easier processing
            all_examples = []
            for class_ex in class_examples:
                all_examples.extend(class_ex)
            
            few_shot_scenarios.append((all_examples, test_cases))
        
        return few_shot_scenarios

